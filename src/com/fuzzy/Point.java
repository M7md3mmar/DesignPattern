package com.fuzzy;

public class Point {
    public Point() {
    }
    
    protected float x;
    protected float y;

    public void setX(float x) {
        this.x = x;
    }

    public float getX() {
        return x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getY() {
        return y;
    }
}
